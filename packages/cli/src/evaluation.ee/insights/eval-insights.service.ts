import { Agent } from '@n8n/agents';
import type { AiInsightsPayload, AiInsightsResponse } from '@n8n/api-types';
import { aiInsightsPayloadSchema, aiInsightsResponseSchema } from '@n8n/api-types';
import { LicenseState, Logger } from '@n8n/backend-common';
import type { TestRun, User } from '@n8n/db';
import { EvaluationCollectionRepository } from '@n8n/db';
import { Service } from '@n8n/di';

import { BadRequestError } from '@/errors/response-errors/bad-request.error';
import { ForbiddenError } from '@/errors/response-errors/forbidden.error';
import { NotFoundError } from '@/errors/response-errors/not-found.error';
import { Telemetry } from '@/telemetry';

import { InsightsContextBuilder } from './insights-context-builder';
import type { InsightsContext, InsightsContextVersion } from './insights-context-builder';
import { InsightsModelResolver } from './insights-model-resolver';
import { averageNormalizedScore, normalizedScores } from './insights-scoring';

// System prompt for the insights agent. Kept terse; the per-collection facts
// (scores, node diffs, regressed cases) arrive in the user prompt, and the
// output shape is enforced by `structuredOutput(aiInsightsPayloadSchema)`.
const INSIGHTS_SYSTEM_PROMPT = [
	'You are an evaluation analyst for n8n workflow evaluations.',
	'You compare workflow versions run against the same dataset and explain the results.',
	'Given the JSON context, produce:',
	'- `winner`: set `versionLabel` to the base version letter given in the context; explain why it leads.',
	'- `regressions`: versions that scored notably worse than the winner on a specific metric',
	'  (empty array if none). `delta` is the percentage-point difference vs the winner (negative = worse).',
	'- `suggestedNext`: one concrete next experiment, with a testable `hypothesis`.',
	'Ground every statement in the provided metric scores, workflow node diffs, and regressed cases —',
	'cite metric names and the specific node changes. Scores are percentages (0–100).',
	'Keep each headline under 120 characters and each body under 280 characters.',
].join('\n');

/**
 * Identifier reported in telemetry + the `modelUsed` field of the response
 * when generation falls through to the deterministic path. The frontend
 * uses this to gate any "Generated by AI" labelling — `deterministic` is
 * NOT an AI label, even though the response shape matches.
 */
export const DETERMINISTIC_MODEL_TAG = 'deterministic';

/**
 * Per-run summary used to select the winner and seed both the LLM prompt input
 * and the deterministic fallback. `testRunId` lets the context builder load
 * per-case executions for the LLM path.
 */
type RunSummary = {
	testRunId: string;
	versionLabel: string;
	workflowVersionId: string | null;
	avgScore: number | null;
	// Per-metric scores normalized to [0, 1] (operational metrics excluded).
	// Winner/regression comparisons run over these, not the raw metrics, so the
	// insights talk about actual quality scores rather than token totals.
	scores: Record<string, number>;
};

/**
 * Generates AI insights for an evaluation collection: license gate → load
 * collection → cache short-circuit → require ≥2 scored runs → call the LLM via
 * `invokeAgent()` → validate against `aiInsightsPayloadSchema` → cache.
 *
 * The LLM reuses the collection's eval-config judge model + credential
 * (resolved by {@link InsightsModelResolver}) and reasons over the winner's
 * scores, per-version node diffs, and top regressed cases (assembled by
 * {@link InsightsContextBuilder}). When no supported judge model is configured,
 * or the model errors / returns invalid output after a retry, the service falls
 * back to the deterministic summary (`status: 'fallback'`).
 */
@Service()
export class EvalInsightsService {
	constructor(
		private readonly collectionRepo: EvaluationCollectionRepository,
		private readonly licenseState: LicenseState,
		private readonly telemetry: Telemetry,
		private readonly logger: Logger,
		private readonly modelResolver: InsightsModelResolver,
		private readonly contextBuilder: InsightsContextBuilder,
	) {}

	async generateInsights(
		user: User,
		workflowId: string,
		collectionId: string,
		options: { forceRegenerate?: boolean } = {},
	): Promise<AiInsightsResponse> {
		// 1. License gate. AI Assistant entitlement is the same one gating
		// the rest of the AI feature surface; off-license tenants don't get
		// insights at all.
		if (!this.licenseState.isAiAssistantLicensed()) {
			throw new ForbiddenError('AI Assistant license required for eval-collection insights');
		}

		// 2. Load collection + runs. Single round-trip for the runs we'll
		// summarise.
		const detail = await this.collectionRepo.getDetailByIdAndWorkflowId(collectionId, workflowId);
		if (!detail) {
			throw new NotFoundError('Collection not found');
		}

		// 3. Cache hit short-circuit. Cached payload is the full envelope so
		// `status` / `generatedAt` / `modelUsed` round-trip unchanged.
		if (!options.forceRegenerate && detail.collection.insightsCache) {
			const cached = detail.collection.insightsCache as AiInsightsResponse;
			// Validate cache shape — if a previous run's schema diverged we'd
			// rather regenerate than hand a malformed envelope to the FE.
			const parsed = aiInsightsResponseSchema.safeParse(cached);
			if (parsed.success) return parsed.data;
			this.logger.warn('Cached insights failed schema validation; regenerating', {
				collectionId,
			});
		}

		// 4. Need at least two completed runs with metrics to say anything
		// useful. Compare-of-one isn't a comparison.
		//
		// Labels (A/B/C) are derived from the run's index in the *full*
		// collection — `detail.runs` is ordered `createdAt ASC` (see
		// `EvaluationCollectionRepository.getDetailByIdAndWorkflowId`). The
		// frontend assigns labels by the same position, so if a middle run
		// is filtered out here (still running, errored, or missing metrics)
		// we must keep the later runs on their original positions —
		// otherwise the insights response calls the third run "B" while the
		// frontend shows it as "C", and the user sees the winner / regression
		// attributed to the wrong workflow version.
		const summaries: RunSummary[] = [];
		detail.runs.forEach((run, originalIndex) => {
			if (run.status === 'completed' && run.metrics) {
				summaries.push(this.summariseRun(run, originalIndex));
			}
		});
		if (summaries.length < 2) {
			throw new BadRequestError(
				'Collection needs at least 2 completed runs with metrics before insights can be generated',
			);
		}
		const startMs = Date.now();
		let response: AiInsightsResponse;

		try {
			const winner = this.pickWinner(summaries);
			if (!winner) throw new Error('No scored runs to summarise');
			const { payload, modelId } = await this.invokeAgent({
				user,
				workflowId,
				collectionName: detail.collection.name,
				evaluationConfigId: detail.collection.evaluationConfigId,
				summaries,
				winner,
			});
			response = {
				generatedAt: new Date().toISOString(),
				modelUsed: modelId,
				status: 'ok',
				insights: payload,
			};
		} catch (error) {
			this.logger.debug('Insights agent unavailable; falling back to deterministic summary', {
				collectionId,
				error: error instanceof Error ? error.message : String(error),
			});
			response = {
				generatedAt: new Date().toISOString(),
				modelUsed: DETERMINISTIC_MODEL_TAG,
				status: 'fallback',
				insights: this.buildDeterministicInsights(summaries),
			};
		}

		await this.collectionRepo.updateInsightsCache(collectionId, response);

		this.telemetry.track('Eval collection insights generated', {
			user_id: user.id,
			workflow_id: workflowId,
			collection_id: collectionId,
			model_used: response.modelUsed,
			duration_ms: Date.now() - startMs,
			status: response.status,
			regressions_found: response.insights.regressions.length,
		});

		return response;
	}

	// ---- internals ----

	private summariseRun(run: TestRun, index: number): RunSummary {
		// Label by index letter — A/B/C — so the FE legend chips line up. The
		// agent (and the deterministic path) refer back to these labels.
		const versionLabel = String.fromCharCode(0x41 + index);
		return {
			testRunId: run.id,
			versionLabel,
			workflowVersionId: run.workflowVersionId,
			avgScore: averageNormalizedScore(run.metrics),
			scores: normalizedScores(run.metrics),
		};
	}

	/** Highest-`avgScore` run, or null when nothing scored. */
	private pickWinner(summaries: RunSummary[]): RunSummary | null {
		const scored = summaries.filter(
			(summary): summary is RunSummary & { avgScore: number } => summary.avgScore !== null,
		);
		if (scored.length === 0) return null;
		return scored.reduce((best, summary) => (summary.avgScore > best.avgScore ? summary : best));
	}

	/**
	 * Calls the LLM to produce the insights payload. Reuses the eval-config
	 * judge model + credential and reasons over the winner-relative context.
	 * Throws when no supported model is configured or the output can't be
	 * validated after a retry — the caller then falls back to deterministic.
	 */
	private async invokeAgent(params: {
		user: User;
		workflowId: string;
		collectionName: string;
		evaluationConfigId: string;
		summaries: RunSummary[];
		winner: RunSummary;
	}): Promise<{ payload: AiInsightsPayload; modelId: string }> {
		const { user, workflowId, collectionName, evaluationConfigId, summaries, winner } = params;

		const resolved = await this.modelResolver.resolve(user, workflowId, evaluationConfigId);
		if (!resolved) {
			throw new Error('No supported LLM judge model configured for insights');
		}

		const context = await this.contextBuilder.build(workflowId, {
			collectionName,
			winnerLabel: winner.versionLabel,
			versions: summaries.map(
				(summary): InsightsContextVersion => ({
					testRunId: summary.testRunId,
					workflowVersionId: summary.workflowVersionId,
					versionLabel: summary.versionLabel,
					avgScore: summary.avgScore,
					scores: summary.scores,
				}),
			),
		});

		const agent = new Agent('eval-insights')
			.model(resolved.modelConfig)
			.instructions(INSIGHTS_SYSTEM_PROMPT)
			.structuredOutput(aiInsightsPayloadSchema);

		const payload = await this.generateValidated(agent, this.buildUserPrompt(context));
		return { payload: this.reconcile(payload, context), modelId: resolved.modelId };
	}

	// Keep the model's output consistent with the deterministically-chosen
	// baseline and the data it was given: force the winner to the base version,
	// and keep only regressions the context scores actually support — a known,
	// non-base version that really scored below the base on the named metric.
	// Drops hallucinated labels/metrics and wrong-direction claims so the copy
	// can't contradict the scores + diff rendered on the same card.
	private reconcile(payload: AiInsightsPayload, context: InsightsContext): AiInsightsPayload {
		const versionByLabel = new Map(context.versions.map((version) => [version.label, version]));
		const base = versionByLabel.get(context.baseVersionLabel);
		return {
			...payload,
			winner: { ...payload.winner, versionLabel: context.baseVersionLabel },
			regressions: payload.regressions.filter((regression) => {
				if (regression.versionLabel === context.baseVersionLabel) return false;
				const versionScore = versionByLabel.get(regression.versionLabel)?.metricScores[
					regression.metric
				];
				const baseScore = base?.metricScores[regression.metric];
				return (
					typeof versionScore === 'number' &&
					typeof baseScore === 'number' &&
					versionScore < baseScore
				);
			}),
		};
	}

	// Generate + validate against the payload schema, with one stricter retry —
	// a real model's structured output can occasionally drift from the shape.
	private async generateValidated(agent: Agent, userPrompt: string): Promise<AiInsightsPayload> {
		const attempt = async (prompt: string) => {
			const result = await agent.generate(prompt);
			return aiInsightsPayloadSchema.safeParse(result.structuredOutput);
		};

		const first = await attempt(userPrompt);
		if (first.success) return first.data;

		const retry = await attempt(
			`${userPrompt}\n\nReturn ONLY a JSON object matching the required schema exactly — no extra keys, no prose.`,
		);
		if (retry.success) return retry.data;

		throw new Error('LLM insights output failed schema validation after retry');
	}

	private buildUserPrompt(context: InsightsContext): string {
		return [
			'Compare these workflow versions, all evaluated against the same dataset.',
			`The base version "${context.baseVersionLabel}" is the winner — write the winner card about it`,
			'and set winner.versionLabel to that letter. Scores are percentages (0–100). Ground every',
			'statement in the metric scores, workflow node diffs, and regressed cases below.',
			'',
			JSON.stringify(context),
		].join('\n');
	}

	/**
	 * Computes insights deterministically from raw per-version aggregates.
	 * No LLM involved — every value here is derivable arithmetically from
	 * the runs' metrics. The agent is expected to produce more nuanced
	 * narrative copy on top of the same underlying facts.
	 *
	 * Algorithm:
	 *  - **Winner** = highest `avgScore`. Ties resolved by first occurrence.
	 *  - **Regressions** = per-version, per-metric drops vs. the winner's
	 *    score on the same metric. Only included if delta ≥ 10 percentage
	 *    points (an arbitrary but visible threshold; spec doesn't pin one).
	 *  - **Suggested next** = generic "compare in isolation" recommendation
	 *    referencing the regressed metric with the largest delta.
	 */
	private buildDeterministicInsights(summaries: RunSummary[]): AiInsightsPayload {
		const scored = summaries.filter(
			(s): s is RunSummary & { avgScore: number } => s.avgScore !== null,
		);

		if (scored.length === 0) {
			// No scored runs — return a neutral envelope. Validation upstream
			// already requires ≥2 completed runs, so this branch is mostly
			// defensive for the all-metrics-empty edge case.
			return {
				winner: {
					versionLabel: summaries[0]?.versionLabel ?? 'A',
					headline: 'No scored runs',
					body: 'No runs in this collection produced numeric metrics yet.',
				},
				regressions: [],
				suggestedNext: {
					headline: 'Re-run with metric outputs configured',
					body: 'Add evaluation set-metrics nodes to the workflow so insights can compare runs.',
					hypothesis: 'Without numeric metrics there is nothing to compare across versions.',
				},
			};
		}

		const winner = scored.reduce((best, s) => (s.avgScore > best.avgScore ? s : best));
		const regressions = this.collectRegressions(scored, winner);
		const suggestedNext = this.composeSuggestedNext(winner, regressions);

		return {
			winner: {
				versionLabel: winner.versionLabel,
				headline: `${winner.versionLabel} is the winner`,
				body: `${winner.versionLabel} leads on average score (${this.formatScore(winner.avgScore)}) across ${Object.keys(winner.scores).length} metric(s).`,
			},
			regressions,
			suggestedNext,
		};
	}

	private collectRegressions(
		scored: Array<RunSummary & { avgScore: number }>,
		winner: RunSummary & { avgScore: number },
	) {
		// Only include drops ≥10pp on the same metric the winner scored well
		// on. Smaller deltas surface as visual noise rather than insight.
		const REGRESSION_DELTA_THRESHOLD = 0.1;
		const regressions: AiInsightsPayload['regressions'] = [];
		for (const run of scored) {
			if (run.versionLabel === winner.versionLabel) continue;
			for (const [metric, winnerScore] of Object.entries(winner.scores)) {
				const runScore = run.scores[metric];
				if (typeof runScore !== 'number') continue;
				const delta = runScore - winnerScore;
				if (delta >= -REGRESSION_DELTA_THRESHOLD) continue;
				// `delta` is a [-1, 1] score difference; report it as signed
				// percentage points so the copy reads "N percentage points below".
				const deltaPoints = Number((delta * 100).toFixed(1));
				regressions.push({
					versionLabel: run.versionLabel,
					metric,
					delta: deltaPoints,
					headline: `${run.versionLabel} regressed on ${metric}`,
					body: `${run.versionLabel} scored ${this.formatScore(runScore)} on ${metric}, ${Math.abs(deltaPoints).toFixed(1)} percentage points below ${winner.versionLabel}.`,
				});
			}
		}
		return regressions;
	}

	private composeSuggestedNext(
		winner: RunSummary & { avgScore: number },
		regressions: AiInsightsPayload['regressions'],
	): AiInsightsPayload['suggestedNext'] {
		if (regressions.length === 0) {
			return {
				headline: `Lock in ${winner.versionLabel} as the baseline`,
				body: `No version regressed sharply against ${winner.versionLabel}. Promote it and use it as the comparison baseline for future experiments.`,
				hypothesis: `${winner.versionLabel} is a solid starting point; further gains require new variants rather than fixing regressions.`,
			};
		}
		const worst = regressions.reduce((w, r) => (r.delta < w.delta ? r : w));
		return {
			headline: `Investigate ${worst.metric} regression on ${worst.versionLabel}`,
			body: `${worst.versionLabel} lost ${Math.abs(worst.delta).toFixed(1)} percentage points on ${worst.metric} vs ${winner.versionLabel}. Try a variant that keeps ${winner.versionLabel}'s configuration for that metric and changes only the rest.`,
			hypothesis: `If isolating ${winner.versionLabel}'s ${worst.metric} configuration into a new variant recovers the lost points, the rest of ${worst.versionLabel}'s changes were the regression's cause.`,
		};
	}

	private formatScore(score: number): string {
		// Scores are normalized to [0, 1]; surface them as whole-percent so the
		// prose reads "83%" rather than "0.83". Agent prose can override per locale.
		return `${Math.round(score * 100)}%`;
	}
}
