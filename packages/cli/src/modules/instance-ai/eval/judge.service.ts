/**
 * EvalJudgeService — Tool-based workflow execution verifier (TRUST-43/44).
 *
 * Instead of dumping the entire execution trace into one LLM prompt (~50-100K
 * tokens), this service gives the judge a compact summary plus tools to inspect
 * specific nodes on demand. This mirrors the pattern used by mock-handler.ts
 * (lookup_api_docs + get_node_config tools).
 *
 * Tools provided to the judge:
 *   - inspect_node   — full detail for a specific node (config, requests, responses, output)
 *   - get_workflow_structure — all nodes and connections
 *   - list_errors    — all execution errors and pre-analysis flags
 */

import type {
	EvalJudgeVerdict,
	InstanceAiEvalExecutionResult,
	InstanceAiEvalNodeResult,
} from '@n8n/api-types';
import { evalJudgeVerdictSchema } from '@n8n/api-types';
import { Logger } from '@n8n/backend-common';
import { Service } from '@n8n/di';
import type { IWorkflowBase } from 'n8n-workflow';
import { z } from 'zod';

import { createEvalAgent, Tool } from '@n8n/instance-ai';

// ---------------------------------------------------------------------------
// System prompt — adapted from mock-execution-verify.ts for tool-based use
// ---------------------------------------------------------------------------

const JUDGE_SYSTEM_PROMPT = `You are an expert evaluator for n8n workflow execution tests. Workflows are built by an AI agent and executed with LLM-generated mock HTTP responses. Your job is to verify whether the success criteria is satisfied AND diagnose the root cause of any failure.

## How mock execution works

This is a test environment. No real credentials or API connections exist. ALL HTTP calls are intercepted and answered by an LLM mock. This is by design — the purpose is to test the workflow structure and data flow without real services.

- **Mocked nodes**: Made HTTP requests that were intercepted. An LLM generated the response. The node then processed the mock response using its real code. These nodes have NO real credentials — they use mock credentials that allow the node code to run but never reach real APIs.
- **Pinned nodes**: Trigger/start nodes whose output was generated by an LLM to simulate incoming data (webhooks, schedules). They didn't execute — their output was injected directly.
- **Real nodes**: Logic nodes (Code, Set, Merge, Filter, Sort, IF, Switch) that executed their actual code on data from mocked/pinned upstream nodes.

IMPORTANT: Nodes receiving mock responses instead of real API responses is EXPECTED. Missing or mock credentials is EXPECTED. Don't flag these as issues — they are the testing mechanism itself.

## Your tools

You have three tools. Use them to investigate before making your judgment:

"inspect_node" — Returns full detail for a specific node: its configuration, intercepted HTTP requests with mock responses, and output data. Use this to trace data flow through specific nodes.

"get_workflow_structure" — Returns all nodes in the workflow and their connections. Use this to verify node existence and wiring before making claims about missing nodes or wrong connections.

"list_errors" — Returns all execution errors and pre-analysis flags (automated issue detection). Check this first to understand known issues.

## How to evaluate

1. Start by calling "list_errors" to see automated flags and execution errors
2. Read the execution summary in your initial context to understand what ran and what didn't
3. Use "inspect_node" to trace data flow through specific nodes that are relevant to the success criteria
4. Use "get_workflow_structure" if you need to verify wiring or check if a node exists
5. Be definitive, not speculative. You have full access to node details — use them.

## Evaluation principles

- A mock response that faithfully matches the HTTP request is NEVER a mock issue, even if downstream nodes needed different data. If the request didn't ask for a field, the mock shouldn't invent it. The fault lies with the node choice or configuration.
- Always check workflow structure before claiming a node is missing or miswired.
- Workflows can branch. Not every node runs in every execution. A crashed node prevents all downstream branches from running. Identify the single root cause rather than listing each unexecuted downstream node.
- For scenarios with no errors and no output beyond the trigger: this usually means the workflow handled empty data gracefully (no crash = success for empty-input scenarios).

## Failure categories

When the criteria fails, categorize the root cause:
- **builder_issue**: The AI agent misconfigured a node (missing params, wrong settings, wrong routing, missing nodes). Also applies when a filter/code node receives correct input but produces wrong output.
- **mock_issue**: The LLM mock returned incorrect or missing data. Evidence: _evalMockError in responses, mock response shape doesn't match the endpoint.
- **legitimate_failure**: The workflow genuinely doesn't meet the criteria and neither builder nor mock is at fault.
- **framework_issue**: The evaluation framework itself failed — empty trigger content causing cascading failures.
- **verification_gap**: Not enough information to determine.

NOT failure categories:
- Mock credentials instead of real ones — expected
- HTTP responses from LLM mock — expected
- Pinned trigger data — expected

## Output format

Return ONLY a JSON object:

{ "pass": true, "reasoning": "...", "failureCategory": null, "rootCause": null }
{ "pass": false, "reasoning": "...", "failureCategory": "builder_issue", "rootCause": "..." }`;

// ---------------------------------------------------------------------------
// Service
// ---------------------------------------------------------------------------

@Service()
export class EvalJudgeService {
	constructor(private readonly logger: Logger) {}

	/**
	 * Evaluate an execution result against success criteria using a tool-based LLM judge.
	 *
	 * The judge receives a compact summary and uses tools to inspect specific nodes
	 * on demand, keeping token usage proportional to what the judge actually investigates.
	 */
	async judge(
		executionResult: InstanceAiEvalExecutionResult,
		workflow: IWorkflowBase,
		successCriteria: string,
		scenarioHints?: string,
	): Promise<EvalJudgeVerdict> {
		this.logger.info(`[EvalJudge] Starting — criteria: "${successCriteria.slice(0, 100)}"`);
		this.logger.info(
			`[EvalJudge] Execution: ${executionResult.success ? 'success' : 'failed'}, ${Object.keys(executionResult.nodeResults).length} nodes, ${executionResult.errors.length} errors`,
		);

		const preAnalysisFlags = this.buildPreAnalysisFlags(executionResult);
		const summary = this.buildCompactSummary(
			executionResult,
			successCriteria,
			preAnalysisFlags,
			scenarioHints,
		);
		const tools = this.buildTools(executionResult, workflow, preAnalysisFlags);

		try {
			const agent = createEvalAgent('eval-judge', {
				instructions: JUDGE_SYSTEM_PROMPT,
				cache: true,
			}).structuredOutput(evalJudgeVerdictSchema);

			for (const tool of tools) {
				agent.tool(tool);
			}

			this.logger.info(
				'[EvalJudge] Sending to LLM with tools: inspect_node, get_workflow_structure, list_errors',
			);
			const result = await agent.generate(summary);
			const parsed = result.structuredOutput as EvalJudgeVerdict | undefined;

			if (parsed) {
				this.logger.info(
					`[EvalJudge] Verdict: ${parsed.pass ? 'PASS' : 'FAIL'}${parsed.failureCategory ? ` [${parsed.failureCategory}]` : ''} — ${parsed.reasoning.slice(0, 150)}`,
				);
				return {
					pass: parsed.pass,
					reasoning: parsed.reasoning,
					failureCategory: parsed.failureCategory ?? undefined,
					rootCause: parsed.rootCause ?? undefined,
				};
			}

			this.logger.warn(
				'[EvalJudge] Structured output returned null — judge did not produce result',
			);
			return {
				pass: false,
				reasoning: 'Judge failed to produce a parseable verdict',
				failureCategory: 'verification_gap',
			};
		} catch (error: unknown) {
			const message = error instanceof Error ? error.message : String(error);
			this.logger.error(`[EvalJudge] Judge failed: ${message}`);
			return {
				pass: false,
				reasoning: `Judge error: ${message}`,
				failureCategory: 'verification_gap',
			};
		}
	}

	// ── Compact summary builder ───────────────────────────────────────────

	private buildCompactSummary(
		result: InstanceAiEvalExecutionResult,
		successCriteria: string,
		preAnalysisFlags: string[],
		scenarioHints?: string,
	): string {
		const sections: string[] = [];

		sections.push('## Success Criteria', '', successCriteria, '');

		if (scenarioHints) {
			sections.push('## Scenario', '', scenarioHints, '');
		}

		// Execution status
		sections.push(
			'## Execution Summary',
			'',
			`**Status:** ${result.success ? 'success' : 'failed'}`,
			`**Errors:** ${result.errors.length}`,
			'',
		);

		// Node list with execution modes — compact overview
		const mockedNodes: string[] = [];
		const pinnedNodes: string[] = [];
		const realNodes: string[] = [];

		for (const [nodeName, nr] of Object.entries(result.nodeResults)) {
			const hasIssues =
				nr.configIssues && Object.keys(nr.configIssues).length > 0 ? ' [CONFIG ISSUES]' : '';
			const hasErrors = this.hasMockError(nr) ? ' [MOCK ERROR]' : '';
			const label = `${nodeName}${hasIssues}${hasErrors}`;

			if (nr.executionMode === 'mocked') mockedNodes.push(label);
			else if (nr.executionMode === 'pinned') pinnedNodes.push(label);
			else realNodes.push(label);
		}

		sections.push('## Nodes', '');
		if (mockedNodes.length > 0)
			sections.push(`**Mocked** (HTTP intercepted): ${mockedNodes.join(', ')}`);
		if (pinnedNodes.length > 0)
			sections.push(`**Pinned** (data injected): ${pinnedNodes.join(', ')}`);
		if (realNodes.length > 0) sections.push(`**Real** (logic executed): ${realNodes.join(', ')}`);
		sections.push('');

		if (preAnalysisFlags.length > 0) {
			sections.push('## Automated Flags', '', ...preAnalysisFlags.map((f) => `- ${f}`), '');
		}

		sections.push(
			'Use the "list_errors" tool to see full error details.',
			'Use "inspect_node" to examine specific nodes.',
			'Use "get_workflow_structure" to see all nodes and connections.',
		);

		return sections.join('\n');
	}

	private buildPreAnalysisFlags(result: InstanceAiEvalExecutionResult): string[] {
		const flags: string[] = [];

		for (const warning of result.hints.warnings) {
			flags.push(`FRAMEWORK: ${warning}`);
		}

		if (Object.keys(result.hints.triggerContent).length === 0) {
			flags.push('FRAMEWORK: Trigger content empty — start node received no input data');
		}

		for (const [nodeName, nr] of Object.entries(result.nodeResults)) {
			if (nr.configIssues && Object.keys(nr.configIssues).length > 0) {
				flags.push(
					`BUILDER: "${nodeName}" missing config: ${Object.values(nr.configIssues).flat().join('; ')}`,
				);
			}
		}

		return flags;
	}

	// ── Tool builders ─────────────────────────────────────────────────────

	private buildTools(
		result: InstanceAiEvalExecutionResult,
		workflow: IWorkflowBase,
		preAnalysisFlags: string[],
	) {
		// Build a lookup for workflow node configs — shared across tools
		const nodeConfigs = new Map<string, { type: string; parameters: Record<string, unknown> }>();
		for (const node of workflow.nodes) {
			if (node.parameters) {
				nodeConfigs.set(node.name, { type: node.type, parameters: node.parameters });
			}
		}

		return [
			this.buildInspectNodeTool(result.nodeResults, nodeConfigs),
			this.buildGetWorkflowStructureTool(workflow, result),
			this.buildListErrorsTool(result, preAnalysisFlags),
		];
	}

	private buildInspectNodeTool(
		nodeResults: Record<string, InstanceAiEvalNodeResult>,
		nodeConfigs: Map<string, { type: string; parameters: Record<string, unknown> }>,
	) {
		return new Tool('inspect_node')
			.description(
				'Get full detail for a specific node: configuration, intercepted HTTP requests with mock responses, and output data. Use the exact node name from the execution summary.',
			)
			.input(
				z.object({
					nodeName: z.string().describe('The exact name of the node to inspect'),
				}),
			)
			.handler(async (input: { nodeName: string }) => {
				this.logger.info(`[EvalJudge] 🔧 inspect_node("${input.nodeName}")`);
				const result = this.formatNodeDetail(input.nodeName, nodeResults, nodeConfigs);
				this.logger.info(`[EvalJudge]    → ${result.split('\n').length} lines`);
				return result;
			})
			.build();
	}

	private buildGetWorkflowStructureTool(
		workflow: IWorkflowBase,
		result: InstanceAiEvalExecutionResult,
	) {
		return new Tool('get_workflow_structure')
			.description(
				'Get all nodes in the workflow and their connections. Shows which nodes executed and which did not.',
			)
			.input(z.object({}))
			.handler(async () => {
				this.logger.info('[EvalJudge] 🔧 get_workflow_structure()');
				const output = this.formatWorkflowStructure(workflow, result);
				this.logger.info(`[EvalJudge]    → ${workflow.nodes.length} nodes`);
				return output;
			})
			.build();
	}

	private buildListErrorsTool(result: InstanceAiEvalExecutionResult, preAnalysisFlags: string[]) {
		return new Tool('list_errors')
			.description(
				'Get all execution errors and automated pre-analysis flags. Call this first to understand known issues.',
			)
			.input(z.object({}))
			.handler(async () => {
				this.logger.info('[EvalJudge] 🔧 list_errors()');
				const output = this.formatErrors(result, preAnalysisFlags);
				this.logger.info(
					`[EvalJudge]    → ${result.errors.length} errors, ${preAnalysisFlags.length} flags`,
				);
				return output;
			})
			.build();
	}

	// ── Tool response formatters ──────────────────────────────────────────

	private formatNodeDetail(
		nodeName: string,
		nodeResults: Record<string, InstanceAiEvalNodeResult>,
		nodeConfigs: Map<string, { type: string; parameters: Record<string, unknown> }>,
	): string {
		const nr = nodeResults[nodeName];
		if (!nr) {
			const available = Object.keys(nodeResults).join(', ');
			return `Node "${nodeName}" not found in execution results. Available nodes: ${available}`;
		}

		const sections: string[] = [`## ${nodeName} [${nr.executionMode}]`];

		// Node configuration from workflow JSON
		const config = nodeConfigs.get(nodeName);
		if (config) {
			sections.push('**Type:** ' + config.type);
			sections.push('**Config:**');
			sections.push('```json', JSON.stringify(config.parameters, null, 2), '```');
		}

		// Config issues
		if (nr.configIssues && Object.keys(nr.configIssues).length > 0) {
			sections.push(`**Config issues:** ${Object.values(nr.configIssues).flat().join('; ')}`);
		}

		// Intercepted requests + mock responses
		for (const req of nr.interceptedRequests) {
			sections.push(`**Request:** ${req.method} ${req.url}`);
			if (req.requestBody) {
				sections.push('```json', JSON.stringify(req.requestBody, null, 2), '```');
			}
			if (req.mockResponse) {
				sections.push('**Mock response:**');
				sections.push('```json', JSON.stringify(req.mockResponse, null, 2), '```');
			}
		}

		// Node output
		if (nr.output !== null && nr.output !== undefined) {
			sections.push('**Output:**');
			sections.push('```json', JSON.stringify(nr.output, null, 2), '```');
		} else {
			sections.push('**Output:** none');
		}

		return sections.join('\n');
	}

	private formatWorkflowStructure(
		workflow: IWorkflowBase,
		result: InstanceAiEvalExecutionResult,
	): string {
		const executedNodes = new Set(Object.keys(result.nodeResults));
		const sections: string[] = ['## All Nodes', ''];

		for (const node of workflow.nodes) {
			const executed = executedNodes.has(node.name);
			const mode = result.nodeResults[node.name]?.executionMode;
			const status = executed ? `EXECUTED (${mode ?? 'unknown'})` : 'DID NOT RUN';
			const disabled = node.disabled ? ' [DISABLED]' : '';
			sections.push(`- **${node.name}** (${node.type}) — ${status}${disabled}`);
		}

		sections.push('', '## Connections', '');
		sections.push('```json', JSON.stringify(workflow.connections, null, 2), '```');

		return sections.join('\n');
	}

	private formatErrors(result: InstanceAiEvalExecutionResult, preAnalysisFlags: string[]): string {
		const sections: string[] = [];

		if (preAnalysisFlags.length > 0) {
			sections.push('## Pre-analysis Flags', '', ...preAnalysisFlags.map((f) => `- ${f}`), '');
		}

		if (result.errors.length > 0) {
			sections.push('## Execution Errors', '', ...result.errors.map((e) => `- ${e}`), '');
		}

		// Mock generation failures
		const mockFailures: string[] = [];
		for (const [nodeName, nr] of Object.entries(result.nodeResults)) {
			for (const req of nr.interceptedRequests) {
				const mockError = this.extractMockError(req.mockResponse);
				if (mockError) {
					mockFailures.push(`"${nodeName}" ${req.method} ${req.url}: ${mockError}`);
				}
			}
		}

		if (mockFailures.length > 0) {
			sections.push('## Mock Generation Failures', '', ...mockFailures.map((f) => `- ${f}`), '');
		}

		if (sections.length === 0) {
			return 'No errors, no flags, no mock failures detected.';
		}

		return sections.join('\n');
	}

	// ── Helpers ───────────────────────────────────────────────────────────

	/** Check if a node result contains a mock generation error */
	private hasMockError(nr: InstanceAiEvalNodeResult): boolean {
		return nr.interceptedRequests.some(
			(req) => this.extractMockError(req.mockResponse) !== undefined,
		);
	}

	/** Extract error message from a mock response, or undefined if not an error */
	private extractMockError(mockResponse: unknown): string | undefined {
		if (typeof mockResponse !== 'object' || mockResponse === null) return undefined;
		const record = mockResponse as Record<string, unknown>;
		if (!('_evalMockError' in record)) return undefined;
		const msg = record.message;
		return typeof msg === 'string' ? msg : 'unknown';
	}
}
