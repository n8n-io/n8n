import type { User } from '@n8n/db';
import {
	discoverSchemasForNode,
	findSchemaForOperation,
	generateJsonSchemaFromData,
	type JsonSchema,
} from '@n8n/workflow-sdk';
import type { Logger } from '@n8n/backend-common';
