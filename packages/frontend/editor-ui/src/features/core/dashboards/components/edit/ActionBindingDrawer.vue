<script setup lang="ts">
import { computed, ref, watch } from 'vue';
import { N8nButton, N8nInput, N8nText, N8nSelect, N8nOption, N8nBadge } from '@n8n/design-system';
import { useWorkflowsListStore } from '@/app/stores/workflowsList.store';
import { useRootStore } from '@n8n/stores/useRootStore';

import type { DashboardAction } from '@/features/core/dashboards/dashboards.types';

type WorkflowSummary = {
	id: string;
	name: string;
	active: boolean;
};

type WebhookNodeSummary = {
	id: string;
	name: string;
	path: string;
	httpMethod: string;
	/** Stable routing UUID from n8n internals. Survives node-id regeneration. */
	webhookId?: string;
};

const props = defineProps<{
	open: boolean;
	projectId: string;
	initial?: DashboardAction | null;
}>();

const emit = defineEmits<{
	(e: 'close'): void;
	(e: 'save', action: DashboardAction): void;
}>();

const rootStore = useRootStore();
const workflowsStore = useWorkflowsListStore();

const slug = ref('');
const label = ref('');
const confirm = ref('');
const payloadShape = ref<'row' | 'rows' | 'custom'>('row');
const selectedWorkflowId = ref('');
const selectedNodeId = ref('');
const workflowOptions = ref<WorkflowSummary[]>([]);
const webhookNodes = ref<WebhookNodeSummary[]>([]);
const loadingWorkflows = ref(false);
const loadingWebhooks = ref(false);
const error = ref('');

watch(
	() => [props.open, props.initial],
	() => {
		if (!props.open) return;
		const a = props.initial;
		slug.value = a?.slug ?? '';
		label.value = a?.label ?? '';
		confirm.value = a?.confirm ?? '';
		payloadShape.value = a?.payloadShape ?? 'row';
		selectedWorkflowId.value = a?.target.workflowId ?? '';
		selectedNodeId.value = a?.target.webhookNodeId ?? '';
		void loadWorkflows();
		if (selectedWorkflowId.value) {
			void loadWebhooks(selectedWorkflowId.value);
		}
	},
	{ immediate: true },
);

async function loadWorkflows() {
	loadingWorkflows.value = true;
	error.value = '';
	try {
		// Use the project-scoped list. Trigger-node filter narrows to workflows
		// that have at least one webhook-style trigger.
		const list = await workflowsStore.fetchWorkflowsPage(props.projectId, 1, 200, undefined, {
			triggerNodeTypes: [
				'n8n-nodes-base.webhook',
				'n8n-nodes-base.formTrigger',
				'n8n-nodes-base.mcpTrigger',
			],
		});
		workflowOptions.value = list.map((w) => ({
			id: w.id,
			name: w.name,
			active: Boolean((w as unknown as { active?: boolean }).active),
		}));
	} catch (e) {
		error.value = e instanceof Error ? e.message : String(e);
	} finally {
		loadingWorkflows.value = false;
	}
}

async function loadWebhooks(workflowId: string) {
	loadingWebhooks.value = true;
	error.value = '';
	webhookNodes.value = [];
	try {
		const workflow = await workflowsStore.fetchWorkflow(workflowId);
		const nodes = workflow.nodes ?? [];
		webhookNodes.value = nodes
			.filter(
				(n) =>
					n.type === 'n8n-nodes-base.webhook' ||
					n.type === 'n8n-nodes-base.formTrigger' ||
					n.type === 'n8n-nodes-base.mcpTrigger',
			)
			.map((n) => {
				const params = (n.parameters ?? {}) as Record<string, unknown>;
				return {
					id: n.id,
					name: n.name,
					path: String(params.path ?? ''),
					httpMethod: String(params.httpMethod ?? 'POST'),
					webhookId: (n as unknown as { webhookId?: string }).webhookId,
				};
			});
	} catch (e) {
		error.value = e instanceof Error ? e.message : String(e);
	} finally {
		loadingWebhooks.value = false;
	}
}

watch(selectedWorkflowId, (id) => {
	if (id) void loadWebhooks(id);
	selectedNodeId.value = '';
});

const resolvedUrl = computed(() => {
	if (!selectedNodeId.value) return '';
	const node = webhookNodes.value.find((n) => n.id === selectedNodeId.value);
	if (!node) return '';
	const base = rootStore.webhookUrl?.replace(/\/$/, '');
	if (!base) return '';
	const path = node.path?.replace(/^\//, '');
	return path ? `${base}/${path}` : base;
});

const canSave = computed(() => slug.value.trim() && label.value.trim() && resolvedUrl.value);

function save() {
	const node = webhookNodes.value.find((n) => n.id === selectedNodeId.value);
	if (!selectedWorkflowId.value || !selectedNodeId.value) return;
	// `webhookUrl` is intentionally NOT persisted — the backend resolves it from
	// workflowId + (webhookId | webhookNodeId) at execution time.
	//
	// We store both `webhookId` (the stable routing UUID — preferred) AND
	// `webhookNodeId` (the node's UI id — legacy fallback). The backend prefers
	// webhookId because node IDs are regenerated by the AI builder on every save.
	const action: DashboardAction = {
		slug: slug.value.trim(),
		label: label.value.trim(),
		confirm: confirm.value.trim() || undefined,
		target: {
			type: 'webhook',
			workflowId: selectedWorkflowId.value,
			webhookId: node?.webhookId,
			webhookNodeId: selectedNodeId.value,
			webhookNodeName: node?.name,
		},
		payloadShape: payloadShape.value,
	};
	emit('save', action);
}

function copyUrl() {
	if (!resolvedUrl.value) return;
	void navigator.clipboard.writeText(resolvedUrl.value);
}
</script>

<template>
	<aside v-if="open" class="action-drawer">
		<header class="action-drawer__header">
			<N8nText tag="h3" size="medium" bold>
				{{ initial ? 'Edit action' : 'New action' }}
			</N8nText>
			<N8nButton type="tertiary" size="mini" icon="circle-x" label="" @click="emit('close')" />
		</header>

		<div class="action-drawer__body">
			<label class="action-drawer__field">
				<N8nText size="small" color="text-light">Slug</N8nText>
				<N8nInput v-model="slug" placeholder="resolve-ticket" />
				<N8nText size="xsmall" color="text-light">
					Lowercase letters, digits, hyphens. Used in the action's URL.
				</N8nText>
			</label>

			<label class="action-drawer__field">
				<N8nText size="small" color="text-light">Label</N8nText>
				<N8nInput v-model="label" placeholder="Resolve" />
			</label>

			<label class="action-drawer__field">
				<N8nText size="small" color="text-light">Confirmation message (optional)</N8nText>
				<N8nInput v-model="confirm" placeholder="Are you sure?" />
			</label>

			<label class="action-drawer__field">
				<N8nText size="small" color="text-light">Payload shape</N8nText>
				<N8nSelect v-model="payloadShape">
					<N8nOption value="row" label="Single row" />
					<N8nOption value="rows" label="Multiple rows" />
					<N8nOption value="custom" label="Custom payload" />
				</N8nSelect>
			</label>

			<div class="action-drawer__divider" />

			<label class="action-drawer__field">
				<N8nText size="small" color="text-light">Workflow</N8nText>
				<N8nSelect
					v-model="selectedWorkflowId"
					:placeholder="loadingWorkflows ? 'Loading…' : 'Pick a workflow'"
					filterable
				>
					<N8nOption
						v-for="wf in workflowOptions"
						:key="wf.id"
						:value="wf.id"
						:label="wf.name + (wf.active ? '' : ' (inactive)')"
					/>
				</N8nSelect>
			</label>

			<label v-if="selectedWorkflowId" class="action-drawer__field">
				<N8nText size="small" color="text-light">Webhook trigger node</N8nText>
				<N8nSelect
					v-model="selectedNodeId"
					:placeholder="
						loadingWebhooks
							? 'Loading…'
							: webhookNodes.length
								? 'Pick a webhook node'
								: 'No webhook nodes in this workflow'
					"
				>
					<N8nOption
						v-for="node in webhookNodes"
						:key="node.id"
						:value="node.id"
						:label="`${node.name} (${node.httpMethod} ${node.path || '—'})`"
					/>
				</N8nSelect>
			</label>

			<div v-if="resolvedUrl" class="action-drawer__url">
				<N8nText size="xsmall" color="text-light">Resolved URL</N8nText>
				<div class="action-drawer__url-row">
					<code>{{ resolvedUrl }}</code>
					<N8nButton
						type="tertiary"
						size="mini"
						icon="clipboard-list"
						label=""
						title="Copy"
						@click="copyUrl"
					/>
				</div>
				<N8nBadge theme="secondary" size="small">production</N8nBadge>
			</div>

			<div v-if="error" class="action-drawer__error">{{ error }}</div>
		</div>

		<footer class="action-drawer__footer">
			<N8nButton type="tertiary" label="Cancel" @click="emit('close')" />
			<N8nButton type="primary" label="Save action" :disabled="!canSave" @click="save" />
		</footer>
	</aside>
</template>

