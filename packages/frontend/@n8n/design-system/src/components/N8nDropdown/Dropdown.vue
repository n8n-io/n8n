<script setup lang="ts" generic="T = string, D = never, U extends IUser = IUser">
import { useDebounceFn } from '@vueuse/core';
import {
	DropdownMenuRoot,
	DropdownMenuTrigger,
	DropdownMenuPortal,
	DropdownMenuContent,
} from 'reka-ui';
import { computed, ref, watch, useCssModule, nextTick, toRef, useAttrs } from 'vue';

import N8nBadge from '../N8nBadge';
import N8nIcon from '../N8nIcon';
import { N8nKeyboardShortcut } from '../N8nKeyboardShortcut';
import N8nLoading from '../../v2/components/Loading/Loading.vue';
import { useMenuKeyboardNavigation } from './composables/useMenuKeyboardNavigation';
import { isAlign, isSide } from './Dropdown.typeguards';
import type {
	DropdownMenuItemProps,
	DropdownMenuPlacement,
	DropdownMenuSlots,
	DropdownMenuTrigger as DropdownTriggerType,
} from './Dropdown.types';
import N8nDropdownItem from './DropdownMenuItem.vue';
import N8nDropdownSearch from './DropdownMenuSearch.vue';
import type { IconOrEmoji } from '../N8nIconPicker/types';
import type { IconName } from '../N8nIcon/icons';
import type { IUser, UserAction } from '../../types';
import { useParentScroll } from '../../composables/useParentScroll';

export interface N8nDropdownOption<V = string | number> {
	label: string;
	value: V;
	disabled?: boolean;
}

interface N8nDropdownProps<T, D = never, U extends IUser = IUser> {
	/** Options for legacy select mode. */
	options?: Array<N8nDropdownOption<T>>;
	/** Placeholder text shown in legacy select mode. */
	placeholder?: string;
	/** Trigger size in legacy select mode. */
	size?: 'small' | 'medium' | 'large';
	/** Optional id for menu content; autogenerated when omitted. */
	id?: string;
	/** Menu items for unified action-menu mode. */
	items?: Array<DropdownMenuItemProps<T, D>>;
	/** Legacy ActionToggle actions alias, remapped to `items`. */
	actions?: Array<UserAction<U>>;
	/** Controlled open state (canonical). */
	modelValue?: boolean;
	/** Initial open state for uncontrolled mode. */
	defaultOpen?: boolean;
	/** Menu placement relative to trigger. */
	placement?: DropdownMenuPlacement;
	/** Trigger behavior. */
	trigger?: DropdownTriggerType;
	/** Activator icon for action-menu mode. */
	activatorIcon?: IconOrEmoji | IconName;
	/** Whether trigger is disabled. */
	disabled?: boolean;
	/** Whether content is rendered in a portal. */
	teleported?: boolean;
	/** Max content height in px or css value. */
	maxHeight?: string | number;
	/** Loading state for menu content. */
	loading?: boolean;
	/** Legacy loading rows alias from old APIs. */
	loadingItemCount?: number;
	/** Loading rows count (canonical). */
	loadingRowCount?: number;
	/** Legacy extra popper class alias. */
	extraPopperClass?: string;
	/** Popper class (canonical). */
	popperClass?: string;
	/** Enables search input in menu. */
	searchable?: boolean;
	/** Placeholder text for search input. */
	searchPlaceholder?: string;
	/** Debounce for search event emission in ms. */
	searchDebounce?: number;
	/** Empty-state text when no items are available. */
	emptyText?: string;
	/** Legacy controlled open state alias from ActionToggle. */
	visible?: boolean;
	/** Deprecated legacy prop kept for compatibility; no-op. */
	visibleChange?: boolean;
	/** Deprecated legacy prop kept for compatibility; no-op. */
	hideArrow?: boolean;
	/** Deprecated legacy prop kept for compatibility; no-op. */
	activatorSize?: 'mini' | 'small' | 'medium' | 'large';
	/** Deprecated legacy prop kept for compatibility; no-op. */
	iconSize?: 'xsmall' | 'small' | 'medium' | 'large';
	/** Activator theme used in action-menu mode. */
	theme?: 'default' | 'dark';
	/** Legacy alias controlling ellipsis orientation. */
	iconOrientation?: 'horizontal' | 'vertical';
	/** Closes action-menu mode when the nearest scrollable parent scrolls. */
	closeOnParentScroll?: boolean;
}

defineOptions({ name: 'N8nDropdown', inheritAttrs: false });

const props = withDefaults(defineProps<N8nDropdownProps<T, D, U>>(), {
	options: () => [],
	items: () => [],
	actions: () => [],
	placeholder: 'Select an option',
	size: 'medium',
	placement: 'bottom',
	trigger: 'click',
	activatorIcon: () => ({ type: 'icon', value: 'ellipsis' }),
	disabled: false,
	teleported: true,
	loading: false,
	loadingItemCount: 3,
	loadingRowCount: undefined,
	searchable: false,
	searchPlaceholder: 'Search...',
	searchDebounce: 300,
	emptyText: 'No items',
	extraPopperClass: undefined,
	popperClass: undefined,
	hideArrow: false,
	activatorSize: 'medium',
	iconSize: 'medium',
	theme: 'default',
	iconOrientation: 'vertical',
	closeOnParentScroll: true,
});

const emit = defineEmits<{
	'update:modelValue': [open: boolean];
	'update:open': [open: boolean];
	select: [value: T];
	action: [value: T];
	search: [searchTerm: string, itemId?: T];
	'submenu:toggle': [itemId: T, open: boolean];
	'visible-change': [open: boolean];
	visibleChange: [open: boolean];
	'item-mouseup': [action: UserAction<U>];
}>();

const slots = defineSlots<DropdownMenuSlots<T, D> & {
	activator?: () => void;
	menuItem?: (props: Record<string, unknown>) => void;
	default?: () => void;
}>();

const attrs = useAttrs();
const $style = useCssModule();

const internalOpen = ref(props.defaultOpen ?? false);
const searchRef = ref<{ focus: () => void } | null>(null);
const contentRef = ref<InstanceType<typeof DropdownMenuContent> | null>(null);
const rootRef = ref<HTMLElement | null>(null);
const defaultTriggerRef = ref<HTMLButtonElement | null>(null);
const searchTerm = ref('');
const openSubMenuIndex = ref(-1);
const selectedOption = ref<T | undefined>();
const controlledOpen = computed(() => props.modelValue ?? props.visible);
const resolvedLoadingRows = computed(() => props.loadingRowCount ?? props.loadingItemCount);
const resolvedPopperClass = computed(() => props.extraPopperClass ?? props.popperClass);

const isOptionsMode = computed(() => props.options.length > 0 && props.items.length === 0 && props.actions.length === 0);
const isActionToggleMode = computed(() => props.actions.length > 0);
const hasLegacyActivatorSlot = computed(() => Boolean(slots.activator));
const hasTriggerSlot = computed(
	() => Boolean(slots.trigger) || hasLegacyActivatorSlot.value || (!isOptionsMode.value && Boolean(slots.default)),
);

const selectedOptionLabel = computed(() => {
	if (selectedOption.value === undefined) {
		return props.placeholder;
	}

	return props.options.find((option) => option.value === selectedOption.value)?.label ?? props.placeholder;
});

const normalizedActivatorIcon = computed<IconOrEmoji>(() => {
	const icon = props.activatorIcon;
	if (typeof icon === 'string') {
		return { type: 'icon', value: icon as IconName };
	}

	if (icon?.type) {
		return icon;
	}

	if (props.iconOrientation === 'horizontal') {
		return { type: 'icon', value: 'ellipsis' };
	}

	return { type: 'icon', value: 'ellipsis-vertical' };
});

const normalizedItems = computed<Array<DropdownMenuItemProps<T, D>>>(() => {
	if (props.actions.length > 0) {
		return props.actions.map((action) => ({
			id: action.value as T,
			label: action.label,
			disabled: action.disabled,
			type: action.type,
		}));
	}

	if (props.items.length > 0) {
		return props.items.map((item) => {
			const normalizedIcon =
				typeof item.icon === 'string' ? ({ type: 'icon', value: item.icon } as IconOrEmoji) : item.icon;
			return {
				...item,
				icon: normalizedIcon,
				class: item.class ?? item.customClass,
			} as DropdownMenuItemProps<T, D>;
		});
	}

	return props.options.map((option) => ({
		id: option.value as T,
		label: option.label,
		disabled: option.disabled,
	}));
});

const itemTestIdPrefix = computed(() => {
	const dataTestId = attrs['data-test-id'];
	return typeof dataTestId === 'string' ? dataTestId : undefined;
});
const menuContentId = `n8n-dropdown-${Math.random().toString(36).slice(2, 10)}`;
const resolvedMenuContentId = computed(() => props.id ?? menuContentId);

const getItemTestId = (id: T) => {
	if (props.actions.length > 0) {
		return `action-${String(id)}`;
	}

	if (itemTestIdPrefix.value) {
		return `${itemTestIdPrefix.value}-item-${String(id)}`;
	}

	return `action-${String(id)}`;
};

const getLegacyMenuItemSlotProps = (item: DropdownMenuItemProps<T, D>) => {
	if (props.items.length === 0) {
		return item;
	}

	return props.items.find((candidate) => candidate.id === item.id) ?? item;
};

const placementParts = computed(() => {
	const [sideValue, alignValue] = props.placement.split('-');
	return {
		side: isSide(sideValue) ? sideValue : 'bottom',
		align: isAlign(alignValue) ? alignValue : 'center',
	};
});

const contentStyle = computed(() => {
	if (props.maxHeight) {
		const maxHeightValue = typeof props.maxHeight === 'number' ? `${props.maxHeight}px` : props.maxHeight;
		return { maxHeight: maxHeightValue, overflowY: 'auto' };
	}
	return {};
});

const hasSubMenu = (item: DropdownMenuItemProps<T, D>): boolean => {
	return (item.children && item.children.length > 0) || !!item.loading || !!item.searchable;
};

const navigation = useMenuKeyboardNavigation({
	items: toRef(() => normalizedItems.value),
	hasSubMenu,
	onSelect: (_index, item) => {
		handleItemSelect(item.id);
	},
	onOpenSubMenu: (index) => {
		openSubMenuIndex.value = index;
	},
	onCloseSubMenu: () => {
		if (openSubMenuIndex.value >= 0) {
			openSubMenuIndex.value = -1;
		}
	},
});

const { highlightedIndex } = navigation;
const { attachScrollListeners, detachScrollListeners } = useParentScroll(rootRef, () => {
	close();
});

const emitOpenEvents = (open: boolean) => {
	emit('update:modelValue', open);
	emit('update:open', open);
	emit('visible-change', open);
	emit('visibleChange', open);
};

const handleOpenChange = (open: boolean) => {
	internalOpen.value = open;
	emitOpenEvents(open);
	if (props.closeOnParentScroll && !isOptionsMode.value) {
		if (open) {
			attachScrollListeners();
		} else {
			detachScrollListeners();
		}
	}

	if (!open) {
		navigation.reset();
		openSubMenuIndex.value = -1;
	}

	if (props.searchable) {
		if (open) {
			void nextTick(() => {
				searchRef.value?.focus();
			});
		} else {
			searchTerm.value = '';
			emit('search', '');
		}
	}
};

const debouncedEmitSearch = useDebounceFn((term: string) => {
	emit('search', term);
}, props.searchDebounce);

const handleSearchUpdate = async (value: string) => {
	searchTerm.value = value;
	await debouncedEmitSearch(value);
};

const handleContentKeydown = (event: KeyboardEvent) => {
	if (!props.searchable) return;
	navigation.handleKeydown(event);
};

const handleSubMenuOpenChange = (index: number, open: boolean) => {
	const item = normalizedItems.value[index];
	if (!item) return;

	emit('submenu:toggle', item.id, open);

	if (open) {
		openSubMenuIndex.value = index;
		navigation.reset();
	} else if (openSubMenuIndex.value === index) {
		openSubMenuIndex.value = -1;
		void nextTick(() => {
			if (props.searchable && searchRef.value) {
				highlightedIndex.value = index;
				searchRef.value.focus();
			} else {
				const contentEl = contentRef.value?.$el as HTMLElement | undefined;
				const menuItems = contentEl?.querySelectorAll('[role="menuitem"]');
				const targetItem = menuItems?.[index] as HTMLElement | undefined;
				targetItem?.focus();
			}
		});
	}
};

const handleItemSearch = (term: string, itemId: T) => {
	emit('search', term, itemId);
};

const handleItemSelect = (value: T) => {
	if (isOptionsMode.value) {
		selectedOption.value = value;
	}

	emit('select', value);
	emit('action', value);

	if (props.actions.length > 0) {
		const selectedAction = props.actions.find((action) => action.value === String(value));
		if (selectedAction) {
			emit('item-mouseup', selectedAction);
		}
	}

	close();
};

const handleLegacyClick = (event: MouseEvent) => {
	if (!isActionToggleMode.value) return;

	event.stopPropagation();
	event.preventDefault();

	if (event.target !== event.currentTarget) return;

	// Preserve ActionToggle behavior where clicking the wrapper opens the menu.
	// Triggering the actual button keeps internal Reka state (focus/pointer handling) in sync.
	defaultTriggerRef.value?.click();
};

const open = () => {
	if (props.disabled) return;
	internalOpen.value = true;
	emitOpenEvents(true);
	if (props.closeOnParentScroll && !isOptionsMode.value) {
		attachScrollListeners();
	}
};

const close = () => {
	internalOpen.value = false;
	emitOpenEvents(false);
	detachScrollListeners();
	navigation.reset();
	openSubMenuIndex.value = -1;
};

const openActionToggle = (isOpen: boolean) => {
	if (isOpen) {
		open();
	} else {
		close();
	}
};

watch(
	() => controlledOpen.value,
	(newValue) => {
		if (newValue !== undefined) {
			internalOpen.value = newValue;
			if (props.closeOnParentScroll && !isOptionsMode.value) {
				if (newValue) {
					attachScrollListeners();
				} else {
					detachScrollListeners();
				}
			}
			if (!newValue) {
				navigation.reset();
				openSubMenuIndex.value = -1;
			}
		}
	},
	{ immediate: true },
);

watch(
	() => normalizedItems.value,
	() => {
		navigation.reset();
	},
);

defineExpose({
	open,
	close,
	openActionToggle,
	scrollIntoView: (options?: ScrollIntoViewOptions) => {
		(contentRef.value?.$el as HTMLElement | undefined)?.scrollIntoView(options);
	},
});
</script>

<template>
	<span ref="rootRef" v-bind="attrs" @click="handleLegacyClick">
		<DropdownMenuRoot :open="internalOpen" :modal="false" @update:open="handleOpenChange">
		<DropdownMenuTrigger
			v-if="hasTriggerSlot"
			as-child
			:disabled="disabled"
			data-test-id="action-toggle"
			:aria-controls="resolvedMenuContentId"
		>
			<slot v-if="slots.trigger" name="trigger" />
			<slot v-else-if="slots.activator" name="activator" />
			<slot v-else />
		</DropdownMenuTrigger>
		<DropdownMenuTrigger
			v-else
			as-child
			:disabled="disabled"
		>
			<button
				ref="defaultTriggerRef"
				type="button"
				:aria-controls="resolvedMenuContentId"
			:data-test-id="isOptionsMode ? 'dropdown-trigger' : 'action-toggle'"
			:class="[
				isOptionsMode ? [$style.defaultTrigger, $style[size]] : $style.activator,
				{ [$style.darkActivator]: theme === 'dark' },
			]"
			>
			<template v-if="isOptionsMode">
				<span :class="$style.triggerText">{{ selectedOptionLabel }}</span>
				<N8nIcon icon="chevron-down" :class="[$style.triggerIcon, { [$style.open]: internalOpen }]" size="large" />
			</template>
			<template v-else>
				<N8nIcon
					v-if="normalizedActivatorIcon?.type === 'icon'"
					:icon="normalizedActivatorIcon.value"
					size="large"
				/>
				<span v-else-if="normalizedActivatorIcon?.type === 'emoji'" :class="$style['activator-emoji']">
					{{ normalizedActivatorIcon.value }}
				</span>
			</template>
			</button>
		</DropdownMenuTrigger>

		<component :is="teleported ? DropdownMenuPortal : 'template'">
			<DropdownMenuContent
				v-if="internalOpen"
				:id="resolvedMenuContentId"
				ref="contentRef"
				:class="[$style.content, resolvedPopperClass]"
				:data-test-id="!isOptionsMode ? 'action-toggle-dropdown' : undefined"
				data-menu-content
				:side="placementParts.side"
				:align="placementParts.align"
				:side-offset="5"
				:style="contentStyle"
				:prioritize-position="true"
				@keydown="handleContentKeydown"
				@mouseleave="navigation.reset"
			>
				<slot v-if="slots.content" name="content" />
				<template v-else>
					<N8nDropdownSearch
						v-if="searchable"
						ref="searchRef"
						:model-value="searchTerm"
						:placeholder="searchPlaceholder"
						@update:model-value="handleSearchUpdate"
						@key:escape="close"
						@key:navigate="navigation.navigate"
						@key:arrow-right="navigation.handleArrowRight"
						@key:arrow-left="navigation.handleArrowLeft"
						@key:enter="navigation.handleEnter"
					>
						<template v-if="slots['search-prefix']" #search-prefix>
							<slot name="search-prefix" />
						</template>
						<template v-if="slots['search-suffix']" #search-suffix>
							<slot name="search-suffix" />
						</template>
					</N8nDropdownSearch>

					<div :class="$style['items-container']" data-menu-items>
						<template v-if="loading">
							<slot name="loading">
								<N8nLoading
									v-for="i in resolvedLoadingRows"
									:key="i"
									:rows="1"
									:class="$style['loading-item']"
									variant="p"
								/>
							</slot>
						</template>
						<template v-else-if="normalizedItems.length === 0">
							<slot name="empty">
								<div :class="$style['empty-state']">{{ emptyText }}</div>
							</slot>
						</template>
						<template v-else>
							<template v-for="(item, index) in normalizedItems" :key="item.id">
								<slot name="item" :item="item">
									<N8nDropdownItem
										v-bind="item"
										:highlighted="highlightedIndex === index"
										:sub-menu-open="openSubMenuIndex === index"
										:divided="item.divided && index > 0"
										:data-test-id="getItemTestId(item.id)"
										@select="handleItemSelect"
										@search="handleItemSearch"
										@update:sub-menu-open="(open: boolean) => handleSubMenuOpenChange(index, open)"
									>
										<template v-if="slots['item-leading']" #item-leading="slotProps">
											<slot name="item-leading" v-bind="slotProps" />
										</template>
										<template v-if="slots['item-label']" #item-label="slotProps">
											<slot name="item-label" v-bind="slotProps" />
										</template>
										<template v-else-if="slots.menuItem" #item-label="slotProps">
											<slot name="menuItem" v-bind="getLegacyMenuItemSlotProps(slotProps.item)" />
										</template>
										<template #item-trailing="slotProps">
											<slot v-if="slots['item-trailing']" name="item-trailing" v-bind="slotProps" />
											<template v-else>
												<N8nBadge
													v-if="slotProps.item.badge"
													theme="primary"
													size="xsmall"
													v-bind="slotProps.item.badgeProps"
													:class="slotProps.ui.class"
												>
													{{ slotProps.item.badge }}
												</N8nBadge>
												<N8nKeyboardShortcut
													v-if="slotProps.item.shortcut"
													v-bind="slotProps.item.shortcut"
													:class="slotProps.ui.class"
												/>
												<N8nIcon
													v-if="slotProps.item.type === 'external-link'"
													icon="external-link"
													size="xsmall"
													:class="slotProps.ui.class"
												/>
											</template>
										</template>
									</N8nDropdownItem>
								</slot>
							</template>
						</template>
					</div>
				</template>
			</DropdownMenuContent>
		</component>
		</DropdownMenuRoot>
	</span>
</template>

