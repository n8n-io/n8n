import { expect } from '@playwright/test';
import type { TestInfo } from '@playwright/test';
import autocannon from 'autocannon';
import type { ServiceHelpers } from 'n8n-containers/services/types';

import { reportDiagnostics, reportPgQueryBreakdown, setupBenchmarkRun } from './orchestration';
import type { ApiHelpers } from '../../../../services/api-helper';
import {
	attachThroughputResults,
	getBaselineCounter,
	resolveMetricQuery,
	waitForThroughput,
} from '../../../../utils/benchmark';
import type { BenchmarkDimensions } from '../../../../utils/benchmark';
import type { WebhookHandle } from '../../../../utils/benchmark/webhook-driver';
import { attachMetric } from '../../../../utils/performance-helper';

export interface WebhookThroughputOptions {
	handle: WebhookHandle;
	api: ApiHelpers;
	services: ServiceHelpers;
	testInfo: TestInfo;
	baseUrl: string;
	connections: number;
	durationSeconds: number;
	timeoutMs: number;
	/** PromQL metric to track workflow completions. Defaults to resolveMetricQuery(testInfo). */
	metricQuery?: string;
	plan?: { memory: number; cpu: number };
	workerPlan?: { memory: number; cpu: number };
}

/**
 * Runs a webhook throughput test using autocannon for HTTP load generation
 * and VictoriaMetrics for workflow completion tracking.
 *
 * Phases:
 *   setup (create + activate, via shared helper) → warm up (verify webhook responds)
 *   → baseline → autocannon + VictoriaMetrics in parallel → diagnostics → report.
 *
 * Webhook differs from kafka harnesses: load is generated by autocannon (HTTP)
 * rather than via a TriggerHandle.publishAtRate. The shared `executeLoad`
 * dispatch isn't a fit because autocannon owns its own load model.
 */
export async function runWebhookThroughputTest(options: WebhookThroughputOptions): Promise<void> {
	const { handle, api, services, testInfo, baseUrl, connections, durationSeconds, timeoutMs } =
		options;
	const { nodeCount, nodeOutputSize } = handle.scenario;
	const metricQuery = options.metricQuery ?? resolveMetricQuery(testInfo);
	testInfo.setTimeout(timeoutMs + 120_000);

	const obs = services.observability;

	const dimensions: BenchmarkDimensions = {
		trigger: 'webhook',
		nodes: nodeCount,
		connections,
		duration_s: durationSeconds,
	};
	if (nodeOutputSize !== undefined) dimensions.output = nodeOutputSize;

	// Setup: create workflow + activate. The webhook handle has no waitForReady
	// (warm-up is a separate HTTP probe below), so it skips that step.
	const setup = await setupBenchmarkRun({
		api,
		services,
		testInfo,
		handle,
		metricQuery,
		createOptions: { webhookPrefix: 'bench' },
	});

	const webhookPath = setup.webhookPath;
	if (!webhookPath) {
		throw new Error('Webhook path was not returned from createWorkflowFromDefinition');
	}
	const webhookUrl = `${baseUrl}/webhook/${webhookPath}`;

	// Warm-up: verify webhook responds (retries for async registration).
	await api.webhooks.trigger(`/webhook/${webhookPath}`, {
		method: 'POST',
		data: handle.payload,
		maxNotFoundRetries: 10,
		notFoundRetryDelayMs: 500,
	});
	console.log(`[WEBHOOK] Warm-up complete, webhook registered at /webhook/${webhookPath}`);

	// Baseline must be taken AFTER warm-up so the warm-up POST doesn't get counted.
	const baselineCounter = await getBaselineCounter(obs.metrics, metricQuery);

	// Run autocannon + VictoriaMetrics measurement in parallel
	console.log(
		`[WEBHOOK] Starting ${connections} connections × 2 pipelining for ${durationSeconds}s → ${webhookUrl}\n` +
			`  Workflow: ${nodeCount} nodes (${nodeOutputSize})`,
	);

	const [cannonResult, throughputResult] = await Promise.all([
		autocannon({
			url: webhookUrl,
			connections,
			// 3 in-flight requests per connection — triples effective concurrency
			// (Little's Law: throughput = in_flight / latency) without tripling sockets.
			pipelining: 3,
			duration: durationSeconds,
			method: 'POST',
			body: JSON.stringify(handle.payload),
			headers: { 'Content-Type': 'application/json' },
		}),
		waitForThroughput(obs.metrics, {
			expectedCount: Infinity,
			nodeCount,
			timeoutMs: (durationSeconds + 30) * 1000,
			baselineValue: baselineCounter,
			metricQuery,
		}),
	]);

	// Backlog growth rate: ingestion exceeding processing accumulates queue depth.
	// For async webhooks at saturation, this is THE key indicator of sustainability.
	const httpReqPerSec = cannonResult.requests.average;
	const n8nExecPerSec = throughputResult.tailExecPerSec || throughputResult.avgExecPerSec;
	const backlogGrowthPerSec = httpReqPerSec - n8nExecPerSec;
	const ingestionVsExecutionRatio = n8nExecPerSec > 0 ? httpReqPerSec / n8nExecPerSec : 0;
	const totalErrors = cannonResult.errors + cannonResult.non2xx;
	const errorRatePct =
		cannonResult.requests.total > 0 ? (totalErrors / cannonResult.requests.total) * 100 : 0;

	// Attach metrics: throughput + autocannon HTTP stats
	await attachThroughputResults(testInfo, dimensions, throughputResult);
	await attachMetric(testInfo, 'http-latency-p50', cannonResult.latency.p50, 'ms', dimensions);
	await attachMetric(testInfo, 'http-latency-p99', cannonResult.latency.p99, 'ms', dimensions);
	await attachMetric(
		testInfo,
		'http-requests-total',
		cannonResult.requests.total,
		'count',
		dimensions,
	);
	await attachMetric(testInfo, 'http-requests-avg', httpReqPerSec, 'req/s', dimensions);
	await attachMetric(testInfo, 'http-errors', totalErrors, 'count', dimensions);
	await attachMetric(testInfo, 'http-error-rate-pct', errorRatePct, '%', dimensions);
	await attachMetric(testInfo, 'backlog-growth-per-sec', backlogGrowthPerSec, 'msg/s', dimensions);
	await attachMetric(
		testInfo,
		'ingestion-vs-execution-ratio',
		ingestionVsExecutionRatio,
		'ratio',
		dimensions,
	);

	// Diagnostics — driver-agnostic, shared with kafka harnesses
	const diagnostics = await reportDiagnostics({
		testInfo,
		services,
		durationMs: throughputResult.durationMs,
		dimensions,
	});
	await reportPgQueryBreakdown({ services, durationMs: throughputResult.durationMs });

	const verdict =
		errorRatePct > 1
			? `OVERLOADED — ${errorRatePct.toFixed(2)}% error rate`
			: backlogGrowthPerSec > httpReqPerSec * 0.05
				? `INGESTION > EXECUTION — backlog growing at ${backlogGrowthPerSec.toFixed(1)}/sec`
				: 'BALANCED — ingestion and execution keeping pace';

	console.log(
		`[WEBHOOK RESULT] ${testInfo.title}\n` +
			`  HTTP ingestion:    ${httpReqPerSec.toFixed(1)} req/s | p50: ${cannonResult.latency.p50}ms | p99: ${cannonResult.latency.p99}ms\n` +
			`  n8n execution:     ${n8nExecPerSec.toFixed(1)} exec/s (tail 60s)\n` +
			`  Backlog growth:    ${backlogGrowthPerSec >= 0 ? '+' : ''}${backlogGrowthPerSec.toFixed(1)} msg/sec` +
			` (ingestion is ${ingestionVsExecutionRatio.toFixed(2)}× execution)\n` +
			`  Errors:            ${totalErrors}/${cannonResult.requests.total} (${errorRatePct.toFixed(2)}%)` +
			` | ${cannonResult.errors} timeouts, ${cannonResult.non2xx} non-2xx\n` +
			`  Verdict:           ${verdict}\n` +
			`  PG active conns:   ${diagnostics.pgActiveConnections ?? 'N/A'}` +
			` | PG tx/s: ${diagnostics.pgTxRate?.toFixed(0) ?? 'N/A'}` +
			` | Event loop lag: ${diagnostics.eventLoopLag !== undefined && diagnostics.eventLoopLag !== null ? (diagnostics.eventLoopLag * 1000).toFixed(0) + 'ms' : 'N/A'}\n` +
			`  Duration: ${(throughputResult.durationMs / 1000).toFixed(1)}s`,
	);

	expect(throughputResult.totalCompleted).toBeGreaterThan(0);
}
