import { mkdir, readdir, readFile, writeFile } from 'node:fs/promises';
import { basename, dirname, extname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const packageRoot = fileURLToPath(new URL('..', import.meta.url));
const iconsDir = join(packageRoot, 'src', 'icons');
const outputFile = join(packageRoot, 'src', 'generated', 'node-icons.ts');

const files = (await readdir(iconsDir)).filter((file) => file.endsWith('.svg')).sort();
const entries = await Promise.all(
	files.map(async (file) => {
		const iconName = `node:${basename(file, extname(file))}`;
		const svg = await readFile(join(iconsDir, file), 'utf8');

		return `\t${JSON.stringify(iconName)}: ${JSON.stringify(svg)},`;
	}),
);

await mkdir(dirname(outputFile), { recursive: true });
await writeFile(
	outputFile,
	[
		'// This file is generated by scripts/generate-node-icons.mjs.',
		'',
		'export const nodeIconSet = {',
		...entries,
		'} as const;',
		'',
		'export type NodeIconName = keyof typeof nodeIconSet;',
		'',
	].join('\n'),
);
