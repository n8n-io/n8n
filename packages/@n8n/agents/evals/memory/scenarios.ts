import type { MemoryEvalScenario, MemoryEvalTurn } from './types';

function turns(id: string, expectedCount: number, prompts: string[]): MemoryEvalTurn[] {
	if (prompts.length !== expectedCount) {
		throw new Error(`Scenario ${id} expected ${expectedCount} turns, got ${prompts.length}.`);
	}
	return prompts.map((user) => ({ user }));
}

