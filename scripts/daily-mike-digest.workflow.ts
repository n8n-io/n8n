import { workflow, trigger, node, newCredential, expr } from '@n8n/workflow-sdk';

const SELF_USER_ID = 'U09GV6R3QUX';
const MIKE_SLACK_HANDLE = 'mike';
const MIKE_GITHUB_LOGIN = 'mieky';

const dailyTrigger = trigger({
	type: 'n8n-nodes-base.scheduleTrigger',
	version: 1.3,
	config: {
		name: 'Every day at 9am',
		parameters: {
			rule: {
				interval: [{ field: 'days', daysInterval: 1, triggerAtHour: 9, triggerAtMinute: 0 }],
			},
		},
		position: [240, 300],
	},
	output: [{}],
});

const fetchGithubEvents = node({
	type: 'n8n-nodes-base.httpRequest',
	version: 4.4,
	config: {
		name: 'Fetch GitHub Events',
		parameters: {
			method: 'GET',
			url: `https://api.github.com/users/${MIKE_GITHUB_LOGIN}/events`,
			sendQuery: true,
			queryParameters: { parameters: [{ name: 'per_page', value: '100' }] },
		},
		position: [480, 300],
	},
	output: [
		{ type: 'PushEvent', created_at: '2026-05-11T09:00:00Z', repo: { name: 'n8n-io/n8n' }, payload: { ref: 'refs/heads/feature' } },
	],
});

const searchSlackMessages = node({
	type: 'n8n-nodes-base.slack',
	version: 2.4,
	config: {
		name: 'Search Slack Messages',
		parameters: {
			authentication: 'oAuth2',
			resource: 'message',
			operation: 'search',
			query: expr(
				`from:@${MIKE_SLACK_HANDLE} after:{{ $now.minus({ days: 1 }).toFormat("yyyy-MM-dd") }}`,
			),
			sort: 'desc',
			returnAll: false,
			limit: 50,
		},
		executeOnce: true,
		credentials: { slackOAuth2Api: newCredential('Slack account') },
		position: [720, 300],
	},
	output: [
		{ ts: '1778510522.363919', text: 'sample message', channel: { name: 'team-cloud-platform', is_im: false }, permalink: 'https://example/p1' },
	],
});

const composeDigest = node({
	type: 'n8n-nodes-base.code',
	version: 2,
	config: {
		name: 'Compose Digest Blocks',
		parameters: {
			mode: 'runOnceForAllItems',
			language: 'javaScript',
			jsCode:
				"const cutoff = $now.minus({ days: 1 }).toISO();\n" +
				"const ghAll = $('Fetch GitHub Events').all().map((i) => i.json);\n" +
				"const ghInWindow = ghAll.filter((e) => e.created_at >= cutoff);\n" +
				"const ghCount = ghInWindow.length;\n" +
				"const pushCount = ghInWindow.filter((e) => e.type === 'PushEvent').length;\n" +
				"const prsOpened = ghInWindow.filter((e) => e.type === 'PullRequestEvent' && e.payload && e.payload.action === 'opened');\n" +
				"const branchesCreated = ghInWindow.filter((e) => e.type === 'CreateEvent').map((e) => e.payload && e.payload.ref).filter(Boolean);\n" +
				"\n" +
				"const slack = $input.all().map((i) => i.json);\n" +
				"const byChannel = {};\n" +
				"for (const m of slack) {\n" +
				"  const ch = m && m.channel ? (m.channel.name || (m.channel.is_im ? 'DM' : 'unknown')) : 'unknown';\n" +
				"  byChannel[ch] = (byChannel[ch] || 0) + 1;\n" +
				"}\n" +
				"const channels = Object.entries(byChannel).sort((a, b) => b[1] - a[1]);\n" +
				"\n" +
				"const longest = slack\n" +
				"  .filter((m) => typeof m.text === 'string' && m.text.length > 60)\n" +
				"  .sort((a, b) => b.text.length - a.text.length)\n" +
				"  .slice(0, 8);\n" +
				"\n" +
				"const quoteBlocks = longest.map((m) => {\n" +
				"  const channelLabel = m && m.channel && m.channel.name ? '#' + m.channel.name : (m.channel && m.channel.is_im ? 'DM' : '#?');\n" +
				"  const text = m.text.replace(/[\\r\\n]+/g, ' ').slice(0, 400);\n" +
				"  const link = m.permalink ? ' <' + m.permalink + '|jump>' : '';\n" +
				"  return { type: 'section', text: { type: 'mrkdwn', text: '_' + channelLabel + ':_\\n> ' + text + link } };\n" +
				"});\n" +
				"\n" +
				"const blocks = [\n" +
				"  { type: 'header', text: { type: 'plain_text', text: '\\ud83d\\udcca Mike Arvela \\u2014 daily digest (his own words)', emoji: true } },\n" +
				"  { type: 'context', elements: [\n" +
				"    { type: 'mrkdwn', text: '*Window:* last 24h (since ' + cutoff.slice(0, 16).replace('T', ' ') + ' UTC)' },\n" +
				"    { type: 'mrkdwn', text: '\\u2022' },\n" +
				"    { type: 'mrkdwn', text: 'Generated by scheduled n8n workflow' },\n" +
				"  ] },\n" +
				"  { type: 'divider' },\n" +
				"  { type: 'section', fields: [\n" +
				"    { type: 'mrkdwn', text: '*Slack messages:*\\n' + slack.length },\n" +
				"    { type: 'mrkdwn', text: '*Public GH events:*\\n' + ghCount },\n" +
				"    { type: 'mrkdwn', text: '*Pushes:*\\n' + pushCount },\n" +
				"    { type: 'mrkdwn', text: '*PRs opened:*\\n' + prsOpened.length },\n" +
				"    { type: 'mrkdwn', text: '*Branches:*\\n' + branchesCreated.length },\n" +
				"    { type: 'mrkdwn', text: '*GitHub:*\\n<https://github.com/" + MIKE_GITHUB_LOGIN + "|@" + MIKE_GITHUB_LOGIN + ">' },\n" +
				"  ] },\n" +
				"];\n" +
				"\n" +
				"if (channels.length > 0) {\n" +
				"  blocks.push({ type: 'section', text: { type: 'mrkdwn', text: '*Channel distribution:*\\n' + channels.map(function(p){ return '\\u2022 ' + (p[0] === 'DM' ? 'DM' : '#' + p[0]) + ': ' + p[1]; }).join('\\n') } });\n" +
				"  blocks.push({ type: 'divider' });\n" +
				"}\n" +
				"\n" +
				"if (quoteBlocks.length > 0) {\n" +
				"  blocks.push({ type: 'header', text: { type: 'plain_text', text: 'Direct quotes (verbatim)', emoji: true } });\n" +
				"  for (const b of quoteBlocks) { blocks.push(b); }\n" +
				"  blocks.push({ type: 'divider' });\n" +
				"} else {\n" +
				"  blocks.push({ type: 'section', text: { type: 'mrkdwn', text: '_No substantive messages in the last 24h._' } });\n" +
				"}\n" +
				"\n" +
				"blocks.push({ type: 'context', elements: [\n" +
				"  { type: 'mrkdwn', text: 'Pipeline: scheduleTrigger \\u2192 httpRequest (GitHub) \\u2192 slack.message.search \\u2192 code \\u2192 slack.message.post' },\n" +
				"] });\n" +
				"\n" +
				"return [{ json: {\n" +
				"  blocksUi: JSON.stringify({ blocks }),\n" +
				"  text: 'Mike Arvela daily digest \\u2014 ' + slack.length + ' Slack msgs, ' + ghCount + ' GH events',\n" +
				"} }];",
		},
		position: [960, 300],
	},
	output: [
		{
			blocksUi: '{"blocks":[]}',
			text: 'Mike Arvela daily digest — 0 Slack msgs, 0 GH events',
		},
	],
});

const postDigest = node({
	type: 'n8n-nodes-base.slack',
	version: 2.4,
	config: {
		name: 'DM Filipe',
		parameters: {
			authentication: 'oAuth2',
			resource: 'message',
			operation: 'post',
			select: 'user',
			user: { __rl: true, mode: 'id', value: SELF_USER_ID },
			messageType: 'block',
			text: expr('{{ $json.text }}'),
			blocksUi: expr('{{ $json.blocksUi }}'),
			otherOptions: { includeLinkToWorkflow: false, mrkdwn: true },
		},
		executeOnce: true,
		credentials: { slackOAuth2Api: newCredential('Slack account') },
		position: [1200, 300],
	},
	output: [{ ok: true, channel: 'D09GV6S98HM', ts: '0000000000.000000' }],
});

export default workflow('mike-arvela-daily-digest', 'Mike Arvela — daily digest at 9am')
	.add(dailyTrigger)
	.to(fetchGithubEvents)
	.to(searchSlackMessages)
	.to(composeDigest)
	.to(postDigest);
